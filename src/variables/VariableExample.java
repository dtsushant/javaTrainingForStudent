package variables;

public class VariableExample {

    public static void main(String args[]){

        // declaring a integer variable
        int a;
        // assigning a value
        a = 10;

        System.out.println(a);


        // declaring an integer an and assigning a value
        int b = 100;
        System.out.println(b);

        int c ;

        // operation on a variable
        c = a + b;

        System.out.println(c);


        //this cant be done and will give error
        //a = 9.0;

        float f1,f2,f3;

        f1 = 78.5f;
        System.out.println(f1);

        f2 = 56/11;


        System.out.println(f2);

        f3 = 22.0f/7.0f;
        System.out.println("f3 = "+f3);

        double d1, d2,d3;
        d1 = 9.6;

        d2 = 22.0/7.0;
        System.out.println("d2="+d2);



    }
}
